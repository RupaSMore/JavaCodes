package Ex_20042025;

public class Lab56_vowelsUsingifElse {
    public static void main(String[] args) {
        char ch ='b';
        if(ch=='a'||ch=='e'||ch=='i'||ch=='o'||ch=='u')
        {
            System.out.println("vowel");
        }
        else
        {
            System.out.println("consonant");
        }
    }
}
