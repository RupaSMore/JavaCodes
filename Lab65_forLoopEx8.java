package Ex_21042025;

public class Lab65_forLoopEx8 {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            // i from 1 to 10, 10 times
            if (i == 5) {
                System.out.println("Five");
            } else {
                System.out.println(i);
            }
        }
    }
    }
